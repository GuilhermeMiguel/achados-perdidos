package modelo;

public class DevolucaoObjeto {

	private int id;
	private int idObjeto;
	private String documentoDono;
	private String nomeDono;
	private String telefoneDono;
	private String celularDono;
	private String turmaDono;
	private String turnoDono;
	private String dataDevolucao;
	private String status;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getIdObjeto() {
		return idObjeto;
	}
	public void setIdObjeto(int idObjeto) {
		this.idObjeto = idObjeto;
	}
	public String getDocumentoDono() {
		return documentoDono;
	}
	public void setDocumentoDono(String documentoDono) {
		this.documentoDono = documentoDono;
	}
	public String getNomeDono() {
		return nomeDono;
	}
	public void setNomeDono(String nomeDono) {
		this.nomeDono = nomeDono;
	}
	
	public String getTelefoneDono() {
		return telefoneDono;
	}
	public void setTelefoneDono(String telefoneDono) {
		this.telefoneDono = telefoneDono;
	}
	public String getCelularDono() {
		return celularDono;
	}
	public void setCelularDono(String celularDono) {
		this.celularDono = celularDono;
	}
	public String getTurmaDono() {
		return turmaDono;
	}
	public void setTurmaDono(String turmaDono) {
		this.turmaDono = turmaDono;
	}
	public String getTurnoDono() {
		return turnoDono;
	}
	public void setTurnoDono(String turnoDono) {
		this.turnoDono = turnoDono;
	}
	public String getDataDevolucao() {
		return dataDevolucao;
	}
	public void setDataDevolucao(String dataDevolucao) {
		this.dataDevolucao = dataDevolucao;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}

}
